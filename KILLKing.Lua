        {"K",  1e3},
    }

    local function formatNumber(n)
        for _, s in ipairs(suffixes) do
            local name, val = s[1], s[2]
            if n >= val then
                return string.format("%.2f%s", n / val, name)
            end
        end
        return tostring(math.floor(n))
    end

